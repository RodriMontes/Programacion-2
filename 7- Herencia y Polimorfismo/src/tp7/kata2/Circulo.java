package tp7.kata2;

/**
 *
 * @author vlc
 */
public class Circulo extends Figura {
    private final double radio;

    public Circulo(String nombre, double radio) {
        super(nombre);
        this.radio = radio;
    }

    @Override
    public double calcularArea() {
        return Math.PI * radio * radio;
    }
}

